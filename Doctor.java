package com.appointment.microservice.vo;


public class Doctor {

	private int doctor_id;
	private String doctor_name;
	private String doctor_department;
	private String doctor_role;
	public int getDoctor_id() {
		return doctor_id;
	}
	public void setDoctor_id(int doctor_id) {
		this.doctor_id = doctor_id;
	}
	public String getDoctor_name() {
		return doctor_name;
	}
	public void setDoctor_name(String doctor_name) {
		this.doctor_name = doctor_name;
	}
	public String getDoctor_department() {
		return doctor_department;
	}
	public void setDoctor_department(String doctor_department) {
		this.doctor_department = doctor_department;
	}
	public String getDoctor_role() {
		return doctor_role;
	}
	public void setDoctor_role(String doctor_role) {
		this.doctor_role = doctor_role;
	}
	
	
}
